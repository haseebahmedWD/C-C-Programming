#include<iostream>
#include<cmath>

using namespace std;
int main()
{
    cout<<"Min number between 5 & 10: "<<min(5,10) <<endl;
    cout<<"Max number between 345 & 1000: "<<max(345,1000) <<endl;
    cout<<"Square root of 64: "<<sqrt(64) <<endl;
    cout<<"Round the number 6.4: "<<round(6.4) <<endl;
    cout<<"Round the number 6.5: "<<round(6.5) <<endl;
    cout<<"Log of 2: "<<log(2) <<endl;
    return 0;
}
